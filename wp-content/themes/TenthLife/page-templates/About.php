			<p>Tenth Life Cat Rescue was founded on January 2, 2009, in order to address a lack of resources dedicated to stray cats and kittens in the St. Louis area. While several organizations focused on saving area felines, none possessed the specific mission of prioritizing cats with injuries and special needs about which founder, Elizabeth Frick, was deeply passionate. Starting with just a single foster home, no funding, and Ms. Frick's friends and family, Tenth Life has blossomed into an organization with approximately 80 foster homes in the St. Louis area and 150+ cats in its care at any given time. It has grown through word-of-mouth, a strong presence on social media, and the dedication of the volunteers and supporters who champion its mission. </p>

